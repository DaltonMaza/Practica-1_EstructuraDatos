    public void cargarTabla() {
        if (ec.getEmpresa().getTrabajadorList() != null) {
            mt.setTrabajadores(ec.getEmpresa().getTrabajadorList());
            jTable1.setModel(mt);
            jTable1.updateUI();
        } else {
            JOptionPane.showMessageDialog(null, "Agrege empleados");
        }

    }

    public void limpiar() {
        txtNombresTrabajador.setText("");
        txtApellidos.setText("");
        txtAnio.setText("");
        txtMes.setText("");
        txtDia.setText("");
        txtSueldo.setText("");
    }

    public void guardarEmpresa() {
        if (!txtNombreEmpresa.getText().isEmpty()) {
            ec = new EmpresaController(txtNombreEmpresa.getText());
            cargarTabla();
            JOptionPane.showMessageDialog(null, "Se ha guardado");
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese el nombre de la empresa");
        }
    }

    public void guardarEmpleado() {
        if (txtNombresTrabajador.getText().isEmpty() || txtApellidos.getText().isEmpty()
                || txtSueldo.getText().isEmpty() || txtAnio.getText().isEmpty()
                || txtDia.getText().isEmpty() || txtMes.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese los datos completos");
        } else {
            Calendar c1 = Calendar.getInstance();
            c1.set(Integer.parseInt(txtAnio.getText()), Integer.parseInt(txtMes.getText()), Integer.parseInt(txtDia.getText()));
            Trabajador t1 = new Trabajador(txtNombresTrabajador.getText(), txtApellidos.getText(), Float.parseFloat(txtSueldo.getText()), c1);
            t1.setAniosEmpleo(Utilidades.calcularAniosEmpleo(c1));
            t1.setRango(Utilidades.obtenerRango(t1));
            t1.setBono(Utilidades.obtenerBono(t1.getRango()));
            t1.setSueldoFinal(Utilidades.obtenerSueldoFinal(t1));
            t1.setPagado(false);
            ec.guardarTrabajador(t1);
            limpiar();
            cargarTabla();
            JOptionPane.showMessageDialog(null, "Se ha guardado");
        }
    }

